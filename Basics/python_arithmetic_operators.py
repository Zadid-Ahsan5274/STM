# Python Arithmetic Operators
# Addition: +
# Substraction: -
# Multiplication: *
# Division : /
# Modulo(Remainder): %
# Exponential: **

x = 10
y = 3
z = x + y
print("Addition = "+str(z))
z = x - y
print("Substraction = "+str(z))
z = x * y
print("Multiplication = "+str(z))
z = x / y
print("Division = "+str(z))
z = x % y
print("Modulo = "+str(z))
z = x ** y
print("Exponential = "+str(z))
